    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        // TODO add your handling code here:
        String[] colNames = {"AirportID","Airport Name", "Airport City"};
        List<Airport> airport=Airport.select();
        String[][] returndata = new String[airport.size()][colNames.length];
        int j=0;
        for(Airport a : airport){
            returndata[j][0] = ""+a.getAirportID();
            returndata[j][1] = a.getAirportName();
            returndata[j][2] = a.getAirportCity();
            j++;
        }
        DefaultTableModel model1 = new DefaultTableModel(returndata,colNames){

            @Override
            public boolean isCellEditable(int row, int column) {

                return false;
            }};
            jTable1.setModel(model1);

    }//GEN-LAST:event_jButton7MouseClicked

