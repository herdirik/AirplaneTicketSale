    public void fillpanelAirport(int row){
        int id=Integer.parseInt((String)jTable1.getValueAt(row, 0));
        Airport a =Airport.select(id);
        jTextField1.setText(a.getAirportName());
        jTextField2.setText(a.getAirportCity());
        jLabel11.setText(""+a.getAirportID());
    
    }
    public void fillpanelFlight(int  row){
       int id=Integer.parseInt((String)jTable2.getValueAt(row, 0));
       Flight f =Flight.select(id);
       jComboBox4.setSelectedIndex(f.getAirborne().getAirportID());
       jComboBox5.setSelectedIndex(f.getDestination().getAirportID());
       jComboBox6.setSelectedIndex(f.getAirplane().getAirplaneID());
       jLabel18.setText(""+f.getFlightID());
       SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
       Date date=null;
        try {
            date = format.parse(f.getFlightDate());
        } catch (ParseException ex) {
            Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
       jDateChooser2.setDate(date);
       jTextField5.setText(f.getFlightTime());
    
    }
    public void fillCombobox(){
    
    List<Airport> airports =Airport.select();
        Vector model = new Vector();
        for(Airport a : airports){
           model.addElement(new Item(a.getAirportID(), a.getAirportName()+" ("+a.getAirportCity()+")"));
        }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(model));
        jComboBox2.setModel(new javax.swing.DefaultComboBoxModel(model));
        jComboBox4.setModel(new javax.swing.DefaultComboBoxModel(model));
        jComboBox5.setModel(new javax.swing.DefaultComboBoxModel(model));
    List<Airplane> airplanes = Airplane.select();
        Vector model1 = new Vector();
        for(Airplane ar : airplanes){
            model1.addElement(new Item(ar.getAirplaneID(), ar.getAirplaneType()+"("+ar.getPassengerCapacity()+")"));
        }
       jComboBox3.setModel(new javax.swing.DefaultComboBoxModel(model1));
       jComboBox6.setModel(new javax.swing.DefaultComboBoxModel(model1));
       
    }
   

   

