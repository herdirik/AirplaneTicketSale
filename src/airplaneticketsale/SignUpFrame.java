    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(jTextFieldName.getText().isEmpty() || jTextFieldSurname.getText().isEmpty() || jTextFieldIdentity.getText().isEmpty() || jTextFieldEmail.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Name , surname , Ä±dentity number aren't isempty!!","CARE!!",JOptionPane.WARNING_MESSAGE);
        }
        else
            try{
                int id;
                String number, name, surname, address, bussiness, email, pass;
                Date bdate;
                ResultSet rs = AirplaneTicketTake.dbconnection("INSERT INTO PASSENGERTBL (PASSENGER_IDENTITYNUMBER,PASSENGER_NAME,PASSENGER_SURNAME,PASSENGER_BIRTHDATE,PASSENGER_HOMEADDRESS,"
                        + "PASSENGER_BUSINESS,PASSENGER_EMAIL,PASSENGER_PASSWORD) VALUES('" + jTextFieldIdentity.getText() + "','" + jTextFieldName.getText() + "','" + jTextFieldSurname.getText() + "',"
                        + "'" + jTextFieldBirthdate.getText() + "','" + jTextAreaHomeAdd.getText() + "','" + jTextAreaBussinessAdd.getText() + "','" + jTextFieldEmail.getText() + "',"
                        + "'" + jPasswordField1.getToolTipText() + "')");
                
                while (rs.next()) {
                id = rs.getInt("PASSENGER_ID");
                number = rs.getString("PASSENGER_IDENTITYNUMBER");
                name = rs.getString("PASSENGER_NAME");
                surname = rs.getString("PASSENGER_SURNAME");
                bdate = rs.getDate("PASSENGER_BIRTHDATE");
                address = rs.getString("PASSENGER_HOMEADDRESS");
                bussiness = rs.getString("PASSENGER_BUSINESS");
                email = rs.getString("PASSENGER_EMAIL");
                pass = rs.getString("PASSENGER_PASSWORD");
                passengers.add(new Passenger(id, number, name, surname, bdate.toString(), address, bussiness, email, pass));}

            } catch (SQLException ex) {
                Logger.getLogger(SignUpFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

